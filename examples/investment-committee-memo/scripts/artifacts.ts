export function renderEvidenceHtml(ev: EvidenceMap): string {
  const unresolvedCount = ev.claims.filter((c) => c.review_status === "unresolved").length;

  const claimCards = ev.claims
    .map((c) => {
      const pill = claimPill(c);
      const sources = c.sources.map((s) => `<p class="src">${sourceLine(s)}</p>`).join("\n      ");
      const excerpts = c.sources
        .filter((s) => s.excerpt)
        .map((s) => `<p class="quote">&ldquo;${esc(s.excerpt!)}&rdquo;</p>`)
        .join("\n      ");
      const derived =
        c.verification?.type === "excel_derived"
          ? `<p class="src">How it is derived: ${esc(c.verification.derivation)}.</p>`
          : "";
      return `  <article class="card">
    <div class="card-top">
      <p class="claim-text">&ldquo;${esc(c.rendered_text)}&rdquo;</p>
      <span class="pill ${pill.cls}">${esc(pill.text)}</span>
    </div>
      ${sources}
      ${derived}
      ${excerpts}
      ${c.review_note ? `<p class="verify"><strong>What to verify:</strong> ${esc(c.review_note)}</p>` : ""}
      <p class="meta">Reference ${esc(c.claim_id)}</p>
  </article>`;
    })
    .join("\n");

  const excluded = ev.excluded_sources
    .map(
      (s) => `  <article class="card">
    <div class="card-top">
      <p class="claim-text">${esc(s.title)}</p>
      <span class="pill mute">Excluded — source policy</span>
    </div>
    <p class="src">${esc(s.exclusion_reason ?? "Excluded before drafting.")}</p>
    <p class="meta">Reference ${esc(s.source_id)}</p>
  </article>`,
    )
    .join("\n");

  const body = `
  <div class="stats">
    <div class="stat"><div class="n">${ev.summary.sources_reviewed}</div><div class="t">Sources reviewed</div></div>
    <div class="stat"><div class="n">${ev.summary.claims_total}</div><div class="t">Claims tracked</div></div>
    <div class="stat"><div class="n">${ev.summary.figures_flagged}</div><div class="t">Figures to verify</div></div>
    <div class="stat"><div class="n">${unresolvedCount}</div><div class="t">Unresolved</div></div>
  </div>
  <h2>Every claim, with its source</h2>
  <p class="section-note">Each figure or statement used in the memo appears below with where it came
  from and its review status. Nothing in the draft is unaccounted for.</p>
${claimCards}
  <h2>Excluded from this draft</h2>
  <p class="section-note">Candidate material rejected at source review. No claim in the memo relies on it.</p>
${excluded}`;

  return renderPage({
    title: "Project Alder — Evidence report",
    eyebrow: "Project Alder · Synthetic company · As of " + ev.as_of,
    heading: "Project Alder — Evidence report",
    lede:
      "Every key figure and claim in the draft memo, with its source and review status. " +
      "A reviewer works through the flagged items before sign-off.",
    body,
    footNote:
      "Cell-level tracing applies to the Excel-sourced figures, which are re-checked against the " +
      "model's actual cells on every run. Filing, transcript, note, and CSV references are section- " +
      "or page-level. All data on this page is synthetic and the draft carries no reviewer sign-off.",
  });
}

