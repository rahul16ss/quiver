const SOURCE_TYPE_LABELS: Record<SourceRecord["source_type"], string> = {
  excel_model: "Excel model",
  filing: "Public filing excerpt",
  transcript: "Earnings call transcript",
  internal_note: "Internal note",
  vendor_export: "Data export",
  web: "Web source",
  template: "Template",
};

function formatNumber(raw: string): string {
  const n = Number(raw);
  return Number.isFinite(n) ? n.toLocaleString("en-US") : raw;
}

/** One source, phrased for a business reader. */
function sourceLine(s: SourceRecord): string {
  const typeLabel = SOURCE_TYPE_LABELS[s.source_type] ?? "Source";
  const base = path.basename(s.file);
  const bits: string[] = [];
  if (s.location.sheet && s.location.cell) {
    bits.push(base, `${s.location.sheet} sheet`, `cell ${s.location.cell}`);
    if (s.extracted_value) bits.push(`value ${formatNumber(s.extracted_value)}`);
  } else {
    if (s.source_type === "vendor_export") bits.push(base);
    if (s.location.section && s.location.section !== "n/a") bits.push(s.location.section);
    if (s.location.page !== undefined && !s.location.section?.toLowerCase().includes("section")) {
      bits.push(`page ${s.location.page}`);
    }
  }
  const detail = bits.length > 0 ? ` — ${bits.map(esc).join(" · ")}` : "";
  return `<strong>${esc(typeLabel)}</strong>${detail} <span style="color:#a39d8f">· as of ${esc(s.as_of)}</span>`;
}

/** Plain-language status pill for a claim. */
function claimPill(c: ClaimRecord): { cls: string; text: string } {
  switch (c.review_status) {
    case "verified":
      return c.relationship === "derived"
        ? { cls: "good", text: "Derived — verified" }
        : { cls: "good", text: "Sourced" };
    case "needs_analyst":
      return c.relationship === "derived"
        ? { cls: "warn", text: "Derived — needs analyst" }
        : { cls: "warn", text: "Needs analyst" };
    case "flagged":
      return { cls: "warn", text: "Sourced — verify before use" };
    case "unresolved":
      return { cls: "stop", text: "Unresolved — escalate" };
  }
}

