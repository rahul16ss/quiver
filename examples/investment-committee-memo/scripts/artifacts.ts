export function renderReviewChecklistHtml(ev: EvidenceMap): string {
  const attention = ev.claims.filter(
    (c) => c.review_status === "needs_analyst" || c.review_status === "flagged",
  );
  const unresolved = ev.claims.filter((c) => c.review_status === "unresolved");

  const item = (c: EvidenceClaim, urgent: boolean): string => {
    const pill = claimPill(c);
    const title = c.table?.metric ?? c.rendered_text;
    const sources = c.sources.map((s) => `<p class="src">${sourceLine(s)}</p>`).join("\n        ");
    return `  <div class="check-item${urgent ? " urgent" : ""}">
    <span class="box" aria-hidden="true"></span>
    <div class="item-body">
      <div class="item-head">
        <p class="claim-text">${esc(title)}</p>
        <span class="pill ${pill.cls}">${esc(pill.text)}</span>
      </div>
      <p class="src" style="margin-top:6px">The memo states: &ldquo;${esc(c.rendered_text)}&rdquo;</p>
        ${sources}
      ${c.review_note ? `<p class="verify"><strong>What to verify:</strong> ${esc(c.review_note)}</p>` : ""}
      <p class="meta">Reference ${esc(c.claim_id)}</p>
    </div>
  </div>`;
  };

  const standing = loadChecklist()
    .map((c) => `  <div class="standing"><span class="tick">✓</span><span>${esc(friendlyCheckName(c.id))}</span></div>`)
    .join("\n");

  const body = `
  <h2>Figures to verify (${attention.length})</h2>
  <p class="section-note">Confirm each of these before the committee relies on the draft.</p>
${attention.map((c) => item(c, false)).join("\n")}
  <h2>Unresolved — must be resolved or escalated (${unresolved.length})</h2>
  <p class="section-note">The draft cannot be signed off while this item is open.</p>
${unresolved.map((c) => item(c, true)).join("\n")}
  <h2>Standing checks</h2>
  <p class="section-note">Enforced automatically on every run, before the draft reaches a reviewer.</p>
${standing}`;

  return renderPage({
    title: "Project Alder — Reviewer checklist",
    eyebrow: "Project Alder · Synthetic company · As of " + ev.as_of,
    heading: "Reviewer checklist",
    lede:
      `${attention.length + unresolved.length} items need a human decision before sign-off. ` +
      "Every claim in the draft remains a draft until a reviewer has worked through this list.",
    extraCss: CHECKLIST_CSS,
    body,
    footNote:
      "Review status: draft for review — no reviewer decision is recorded on any item yet. " +
      "All data on this page is synthetic.",
  });
}

